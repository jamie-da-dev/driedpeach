import {Suspense, startTransition} from 'react';
import {Await, NavLink, useAsyncValue} from '@remix-run/react';
import {
  type CartViewPayload,
  useAnalytics,
  useOptimisticCart,
} from '@shopify/hydrogen';
import type {HeaderQuery, CartApiQueryFragment} from 'storefrontapi.generated';
import {useAside} from '~/components/Aside';

// Import React Icons
import {VscAccount} from 'react-icons/vsc';
import {IoIosSearch} from 'react-icons/io';
import {MdOutlineShoppingBag} from 'react-icons/md';
import {IoCartOutline} from 'react-icons/io5';

interface HeaderProps {
  header: HeaderQuery;
  cart: Promise<CartApiQueryFragment | null>;
  isLoggedIn: Promise<boolean>;
  publicStoreDomain: string;
}

type Viewport = 'desktop' | 'mobile';

export function Header({
  header,
  isLoggedIn,
  cart,
  publicStoreDomain,
}: HeaderProps) {
  const {shop, menu} = header;
  return (
    <header className="header">
      {/* Mobile Menu Toggle (only for tablet and mobile screens) */}
      <div className="header-mobile-toggle">
        <HeaderMenuMobileToggle />
      </div>

      {/* Categories Section on the Left (Home, Catalog, Contact) - Only visible on larger screens */}
      <div className="header-categories">
        <HeaderMenu
          menu={menu}
          viewport="desktop"
          primaryDomainUrl={header.shop.primaryDomain.url}
          publicStoreDomain={publicStoreDomain}
        />
      </div>

      {/* Logo Section in the Center */}
      <div className="header-logo">
        <NavLink prefetch="intent" to="/" style={activeLinkStyle} end>
          <strong>{shop.name}</strong>
        </NavLink>
      </div>

      {/* Account, Search, Cart Section on the Right */}
      <div className="header-ctas">
        <HeaderCtas isLoggedIn={isLoggedIn} cart={cart} />
      </div>
    </header>
  );
}

export function HeaderMenu({
  menu,
  primaryDomainUrl,
  viewport,
  publicStoreDomain,
}: {
  menu: HeaderProps['header']['menu'];
  primaryDomainUrl: HeaderProps['header']['shop']['primaryDomain']['url'];
  viewport: Viewport;
  publicStoreDomain: HeaderProps['publicStoreDomain'];
}) {
  const className = `header-menu-${viewport}`;
  const {close} = useAside();

  return (
    <nav className={className} role="navigation">
      {(menu || FALLBACK_HEADER_MENU).items.map((item) => {
        if (!item.url) return null;

        // if the url is internal, we strip the domain
        const url =
          item.url.includes('myshopify.com') ||
          item.url.includes(publicStoreDomain) ||
          item.url.includes(primaryDomainUrl)
            ? new URL(item.url).pathname
            : item.url;

        return (
          <NavLink
            className="header-menu-item"
            end
            key={item.id}
            onClick={close}
            prefetch="intent"
            style={activeLinkStyle}
            to={url}
          >
            {item.title}
          </NavLink>
        );
      })}
    </nav>
  );
}

function HeaderCtas({
  isLoggedIn,
  cart,
}: Pick<HeaderProps, 'isLoggedIn' | 'cart'>) {
  return (
    <nav className="header-ctas" role="navigation">
      {/* Account Icon */}
      <NavLink prefetch="intent" to="/account" style={activeLinkStyle}>
        <Suspense fallback={<VscAccount />}>
          <Await resolve={isLoggedIn} errorElement={<VscAccount />}>
            {(isLoggedIn) => (isLoggedIn ? <VscAccount /> : <VscAccount />)}
          </Await>
        </Suspense>
      </NavLink>

      {/* Search Icon using SearchToggle */}
      <SearchToggle />

      {/* Cart Icon */}
      <Suspense fallback={<CartBadge count={null} />}>
        <Await resolve={cart}>
          {(cartData) => <CartBanner originalCart={cartData} />}
        </Await>
      </Suspense>
    </nav>
  );
}

function HeaderMenuMobileToggle() {
  const {open} = useAside();
  return (
    <button
      className="header-menu-mobile-toggle reset"
      onClick={() => open('mobile')}
    >
      <h3>â˜°</h3>
    </button>
  );
}

function SearchToggle() {
  const {open} = useAside(); // Proper usage of hook at the top level

  return (
    <button className="reset search-button" onClick={() => open('search')}>
      <IoIosSearch />
    </button>
  );
}

function CartBadge({count}: {count: number | null}) {
  const {open} = useAside();
  const {publish, shop, cart, prevCart} = useAnalytics();

  return (
    <a
      href="/cart"
      onClick={(e) => {
        e.preventDefault();
        open('cart');
        publish('cart_viewed', {
          cart,
          prevCart,
          shop,
          url: window.location.href || '',
        } as CartViewPayload);
      }}
      className="cart-icon"
    >
      <MdOutlineShoppingBag />
      {count === null ? <span>&nbsp;</span> : count}
    </a>
  );
}

function CartBanner({
  originalCart,
}: {
  originalCart: CartApiQueryFragment | null;
}) {
  const cart = useOptimisticCart(originalCart);
  return <CartBadge count={cart?.totalQuantity ?? 0} />;
}

